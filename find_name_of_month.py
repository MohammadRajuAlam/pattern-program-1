# find name of month
num_of_month = int(input("Enter number of month: "))
if num_of_month == 1:
    print("January")
elif num_of_month == 2:
    print("February")
elif num_of_month == 3:
    print("March")
elif num_of_month == 4:
    print("April")
elif num_of_month == 5:
    print("May")
elif num_of_month == 6:
    print("June")
elif num_of_month == 7:
    print("July")
elif num_of_month == 8:
    print("August")
elif num_of_month == 9:
    print("September")
elif num_of_month == 10:
    print("October")
elif num_of_month == 11:
    print("November")
elif num_of_month == 12:
    print("December")
else:
    print("Invalid month number")